    <font class="h1"><p><b>Dr. Gerald Straley</b> (1945-1997)

<p>Dr. Gerald Straley was a well known, respected and admired botanical expert on both cultivated plants and the natural flora of British Columbia, and beyond. During his career at UBC, he was a research scientist and curator at the UBC Botanical Garden and the Director of the UBC Herbarium. Dr. Straley also authored several books including, "An Illustrated Flora of the University Endowment Lands", "The Rare Vascular Plants of British Columbia", "The Vascular Plants of British Columbia" and "Trees of Vancouver" (awarded the 1992 Vancouver Book Award for the book best promoting Vancouver). These works were based on Dr. Straley's extensive knowledge of plants and supported by the collection and preservation of plant voucher specimens, as well as botanical line-drawings and field notes. 

